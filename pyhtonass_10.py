"""Q8.Dictionary and counter in Python to find winner of election"""

from collections import Counter

def winner(input):
	votes = Counter(input)

	Dict_1 = {}

	for value in votes.values():
		Dict_1[value] = []

	for (key,value) in votes.items():
		Dict_1[value].append(key)

	maxVote = sorted(Dict_1.keys(),reverse=True)[0]
	if len(Dict_1[maxVote])>1:
		print (sorted(Dict_1[maxVote])[0])
